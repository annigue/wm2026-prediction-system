def generate_group_stage_matches() -> list[dict]:
    matches = []
    start = datetime(2026, 6, 11, 18, 0)
    schedule = [(0, [(0, 2), (1, 3)]), (8, [(0, 3), (1, 2)]), (16, [(0, 1), (2, 3)])]
    match_num = 1
    for g_idx, (group_id, team_ids) in enumerate(sorted(GROUPS.items())):
        group_day_offset = g_idx // 2
        hour_offset = (g_idx % 2) * 3
        primary_venue = GROUP_VENUES[group_id]
        secondary_venue = SECONDARY_VENUES[group_id]
        for md_day_offset, pairs in schedule:
            for pair_idx, (a, b) in enumerate(pairs):
                kickoff = start + timedelta(days=md_day_offset + group_day_offset,
                                            hours=hour_offset + pair_idx * 2)
                venue = primary_venue if pair_idx == 0 else secondary_venue
                matches.append({
                    "id": f"WC2026_GRP{group_id}_G{match_num}", "stage": "GROUP_STAGE",
                    "group_id": group_id, "match_number": match_num,
                    "home_team_id": team_ids[a], "away_team_id": team_ids[b],
                    "venue_id": venue, "kickoff_utc": kickoff, "status": "SCHEDULED",
                })
                match_num += 1
    return matches


